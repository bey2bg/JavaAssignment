
public class Lesson {

	private String name;
	private int grade;
	
	public Lesson() {	}
	
	public void setName(String n)
	{
		this.name=n;
	}
	public void setGrade(int g)
	{
		this.grade=g;
	}
	public String getName()
	{
		return this.name;
	}
	public int getGrade()
	{
		return this.grade;
	}
}
